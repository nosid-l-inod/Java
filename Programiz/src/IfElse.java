// If Else Block

// Compare strings - varX.compareTo(



public class IfElse {
    public static void main(String[] args) {

        // If Else Block
        String name = "Lebron";



        if (name == "Lebron") {
            System.out.println("O nome é lebron");
        }
        else if (name == "Steph") {
            System.out.println("O nome é steph");
        }
        else {
            System.out.println("O nome não existe");
        }


    }
}
