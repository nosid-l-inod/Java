/*
* Abstract class - use "abstract" keyword
* We cannot create objects of abstract classes
* Abstract class can have both regular and abstract methods
* Only abstract classes can have abstract methods
*
* Abstract method
* Does not have a body
*
* We can create subclasses from the abstract classes
*
* A subclass must override all abstract methods and an abstract class
* If the subclass is abstract is not mandatory to override abstract methods
* */



